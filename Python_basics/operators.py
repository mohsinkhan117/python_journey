a = 10
b = 3

print("---- MATHEMATICAL OPERATORS ----")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

print("\n---- ASSIGNMENT OPERATORS ----")

x = 5
print("x =", x)

x = 10
print("x = 10 →", x)

a, b, c = 1, 2, 3
print("Multiple:", a, b, c)

p = q = r = 0
print("Same value:", p, q, r)

print("\n---- COMPOUND ASSIGNMENT ----")

x = 10

x += 5
print("x += 5 →", x)

x -= 3
print("x -= 3 →", x)

x *= 2
print("x *= 2 →", x)

x /= 4
print("x /= 4 →", x)

x //= 2
print("x //= 2 →", x)

x %= 3
print("x %= 3 →", x)

x **= 2
print("x **= 2 →", x)

print("\n---- COMPARISON OPERATORS ----")

a = 5
b = 10

print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

print("\n---- LOGICAL OPERATORS ----")

x = True
y = False

print("AND:", x and y)
print("OR:", x or y)
print("NOT x:", not x)

print("\n---- EXTRA OPERATORS ----")

a = [1, 2]
b = a
c = [1, 2]

print("a is b:", a is b)
print("a is c:", a is c)

nums = [1, 2, 3, 4]

print("2 in nums:", 2 in nums)
print("5 not in nums:", 5 not in nums)


